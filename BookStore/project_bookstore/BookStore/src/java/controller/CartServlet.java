    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("utf-8");
        response.setCharacterEncoding("utf-8");
        HttpSession session = request.getSession();
        Account acc = (Account) session.getAttribute("acc");
        PrintWriter out = response.getWriter();
        if (acc == null) {
            out.println("You must login before use this function!!!");
        } else {
            String qty_raw = request.getParameter("qty");
            String stt_raw = request.getParameter("stt");
            try {
                int qty = Integer.parseInt(qty_raw);
                int stt = Integer.parseInt(stt_raw);
                List<Item> listI =(List<Item>) session.getAttribute("listI");
                if(qty == 0){
                    listI.remove(stt-1);                   
                }
                else listI.get(stt-1).setQuantity(qty);
                session.setAttribute("listI", listI);
                session.setAttribute("NoItem", listI.size());
                response.sendRedirect("cart_process");
                request.getRequestDispatcher("home.jsp").forward(request, response);
                
            } catch (IOException | NumberFormatException e) {
                out.println("Error");
            }
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

