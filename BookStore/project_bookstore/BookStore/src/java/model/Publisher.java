package model;


public class Publisher {
    private String pID, pName;

    public Publisher() {
    }

    public Publisher(String pID, String pName) {
        this.pID = pID;
        this.pName = pName;
    }

    public String getpID() {
        return pID;
    }

    public void setpID(String pID) {
        this.pID = pID;
    }

    public String getpName() {
        return pName;
    }

    public void setpName(String pName) {
        this.pName = pName;
    }
    
}
